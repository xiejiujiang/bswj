package com.example.bswj.Inventorysubscribe;

public class BizContent {

    private String id;
    private String code;
    private String name;
    private String ts;
    public void setId(String id) {
        this.id = id;
    }
    public String getId() {
        return id;
    }

    public void setCode(String code) {
        this.code = code;
    }
    public String getCode() {
        return code;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setTs(String ts) {
        this.ts = ts;
    }
    public String getTs() {
        return ts;
    }

}